def AreaOfCirle(r):
    """
    Area of Circle
    
    Args: 
    r (float) : Area of Circle
  
    Returns:
    float : Area of Circle
    """
    pi = 3.14
    return pi*r*r

def main():
    ## Print Area of Circle
    area = AreaOfCirle(10)
    print(area)


if __name__ == "__main__":
    main()
  
